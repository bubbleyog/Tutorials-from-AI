#!/bin/bash
#
# 脚本名称: 01-hello.sh
# 描述: Shell脚本入门 - Hello World示例
# 用法: ./01-hello.sh [name]
#

# ============================================================
# 严格模式设置（推荐在所有脚本中使用）
# ============================================================
set -euo pipefail

# set -e: 命令失败时立即退出
# set -u: 使用未定义变量时报错
# set -o pipefail: 管道中任何命令失败则整个管道失败

# ============================================================
# 基本输出
# ============================================================
echo "=== Hello World 示例 ==="
echo ""

# 使用echo输出
echo "Hello, World!"

# 使用printf输出（更精确的格式控制）
printf "Hello, %s!\n" "Shell"

# ============================================================
# 带参数的问候
# ============================================================
echo ""
echo "=== 参数化问候 ==="

# 获取脚本名称
script_name=$(basename "$0")
echo "脚本名称: $script_name"

# 检查是否提供了参数
if [ $# -gt 0 ]; then
    name="$1"
    echo "你好, ${name}! 欢迎学习Shell脚本!"
else
    echo "你好, 朋友! 欢迎学习Shell脚本!"
    echo "(提示: 运行 './$script_name 你的名字' 获取个性化问候)"
fi

# ============================================================
# 显示当前环境信息
# ============================================================
echo ""
echo "=== 环境信息 ==="
echo "当前用户: $USER"
echo "主目录: $HOME"
echo "当前目录: $PWD"
echo "Shell: $SHELL"
echo "日期时间: $(date '+%Y-%m-%d %H:%M:%S')"

# ============================================================
# 多行输出示例
# ============================================================
echo ""
echo "=== 多行输出 (Here Document) ==="

cat << 'EOF'
   ____  _          _ _ 
  / ___|| |__   ___| | |
  \___ \| '_ \ / _ \ | |
   ___) | | | |  __/ | |
  |____/|_| |_|\___|_|_|
  
  欢迎开始Shell脚本学习之旅！
EOF

# ============================================================
# 退出状态
# ============================================================
echo ""
echo "脚本执行完成，退出状态: 0（成功）"
exit 0

