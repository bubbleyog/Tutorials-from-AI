#!/bin/bash
#
# 脚本名称: 01-args-basics.sh
# 描述: 位置参数与特殊变量：$0/$1/$#/$@/$*，引号差异，shift与默认值
# 用法:
#   ./01-args-basics.sh a "b c" d
#

set -euo pipefail

echo "=== 位置参数与特殊变量示例 ==="
echo ""

# ============================================================
# 1. 基本信息
# ============================================================
echo "--- 1. 基本信息 ---"

echo "脚本名: $0"
echo "参数个数(\$#): $#"

# ============================================================
# 2. 访问位置参数
# ============================================================
echo ""
echo "--- 2. 访问位置参数 ---"

echo "第1个参数(\$1): ${1:-<空>}"
echo "第2个参数(\$2): ${2:-<空>}"
echo "第3个参数(\$3): ${3:-<空>}"

# 展示 ${10} 的写法（如果不足10个参数则为空）
echo "第10个参数(\${10}): ${10:-<空>}"

# ============================================================
# 3. \"$@\" vs \"$*\" 的差异
# ============================================================
echo ""
echo "--- 3. \"\$@\" vs \"\$*\" 的差异 ---"

echo "原始参数列表(逐个打印):"
idx=0
for arg in "$@"; do
    idx=$((idx + 1))
    echo "  [$idx] = '$arg'"
done

echo ""
echo "\"\$*\" 会把所有参数拼成一个字符串:"
for arg in "$*"; do
    echo "  - '$arg'"
done

# ============================================================
# 4. shift：消费参数
# ============================================================
echo ""
echo "--- 4. shift：消费参数 ---"

args=("$@")

echo "调用 shift 前: $# 个参数"

# 只在存在参数时shift，避免 set -u 下报错
if [[ $# -gt 0 ]]; then
    first="$1"
    shift 1
    echo "消费了第一个参数: '$first'"
else
    echo "没有参数可消费"
fi

echo "调用 shift 后: $# 个参数"

# 把剩余参数重新打印
idx=0
for arg in "$@"; do
    idx=$((idx + 1))
    echo "  剩余[$idx] = '$arg'"
done

# ============================================================
# 5. 默认值与必填参数检查
# ============================================================
echo ""
echo "--- 5. 默认值与必填参数检查 ---"

# 默认值：未提供则使用 default
name=${1:-"default"}

# 必填参数检查示例（注释掉避免脚本直接失败）
# : ${REQUIRED_PARAM:?"REQUIRED_PARAM is missing"}

echo "name(默认值演示): $name"

echo ""
echo "提示：推荐在真实脚本中使用 \"\$@\" 传递参数，避免空格被错误拆分。"
